def red(var):
    """
    =============================
    Returns red str value
    =============================
    """
    return f"\033[31m{var}\033[m"


def green(var):
    """
    =============================
    Returns green str value
    =============================
    """
    return f"\033[32m{var}\033[m"


def yellow(var):
    """
    =============================
    Returns yellow str value
    =============================
    """
    return f"\033[33m{var}\033[m"
