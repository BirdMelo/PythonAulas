# Essa aula é mostrar os operadores aritméticos utilizados no python.

print(3+3)  # soma
print(3-3)  # subtração
print(3*3)  # multipicação
print(3/3)  # divisão
print(3**3)  # potencia
print(3//3)  # divisão inteira
print(3 % 3)  # modulo(resto da divisão)
print(10**(1/2))  # meia potencia de 1 número é a raiz quadrada desse número.
print(10**(1/3))  # 1/3 de potencia de 1 número é a raiz cubica desse número.
print('-'*20)
